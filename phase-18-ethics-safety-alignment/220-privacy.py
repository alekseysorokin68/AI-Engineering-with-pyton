def demo_differential_privacy():
    """Дифференциальная приватность: epsilon, добавление шума, теорема композиции."""
    print("=" * 70)
    print("DEMO 1: Differential Privacy (epsilon, noise, composition)")
    print("=" * 70)

    # --- 1a. Базовый механизм: Laplace noise ---
    print("\n--- Механизм Лапласа ---")
    print("Формула: sensitivity / epsilon → scale параметр распределения Лапласа")
    print("M(D) = f(D) + Lap(Δf / ε)")

    def лаплас_шум(scale):
        """Генерация шума из распределения Лапласа."""
        u = random.random() - 0.5
        return -scale * math.copysign(1, u) * math.log(1 - 2 * abs(u))

    # Подсчёт количества людей с заболеванием
    реальное_число = 42
    чувствительность = 1  # Δf = 1 (один человек меняет счётчик на 1)

    epsilons = [0.1, 0.5, 1.0, 2.0, 5.0, 10.0]
    random.seed(42)

    print(f"\nРеальное значение: {реальное_число}")
    print(f"Чувствительность (Δf): {чувствительность}")
    print(f"\n{'Epsilon':>8s} {'Шум (средн.)':>14s} {'Приватные значения':>20s}")

    for эпсилон in epsilons:
        шумы = [лаплас_шум(чувствительность / эпсилон) for _ in range(1000)]
        средний_шум = sum(abs(s) for s in шумы) / len(шумы)
        приватные = [реальное_число + лаплас_шум(чувствительность / эпсилон) for _ in range(5)]
        приватные_стр = [f"{v:.1f}" for v in приватные[:3]]
        print(f"  {эпсилон:>8.1f} {средний_шум:>14.2f} {', '.join(приватные_стр)}")

    # --- 1b. Epsilon-greedy mechanism ---
    print("\n--- Epsilon-дифференциальная приватность ---")
    print("ε-ДП: для соседних наборов данных D, D': P(M(D) ∈ S) ≤ e^ε × P(M(D') ∈ S)")

    # Два соседних набора (отличаются на одну запись)
    D1 = [25, 30, 35, 40, 45, 50, 55, 60, 65, 70]  # 10 записей
    D2 = [25, 30, 35, 40, 45, 50, 55, 60, 65]       # 9 записей (убрали 70)

    средняя_D1 = sum(D1) / len(D1)
    средняя_D2 = sum(D2) / len(D2)

    print(f"\nD1 (10 записей): среднее = {средняя_D1:.2f}")
    print(f"D2 ( 9 записей): среднее = {средняя_D2:.2f}")
    print(f"Разница средних: {abs(средняя_D1 - средняя_D2):.2f}")

    epsilons_проверка = [0.1, 0.5, 1.0, 2.0]
    for эпсилон in epsilons_проверка:
        bound = math.exp(эпсилон)
        print(f"  ε={эпсилон:.1f}: верхняя граница отношения вероятностей = e^{эпсилон} = {bound:.4f}")

    # --- 1c. Composition Theorem ---
    print("\n--- Теорема композиции ---")
    print("k последовательных запросов с ε → общая приватность = k×ε")
    print("Мягкая композиция: √(k) × ε × √(2×ln(1/δ)) при (ε,δ)-ДП")

    k = 10
    базовый_epsilon = 1.0
    delta = 1e-5

    жёсткая_композиция = k * базовый_epsilon
    мягкая_композиция = math.sqrt(2 * k * math.log(1 / delta)) * базовый_epsilon

    print(f"\nk={k} запросов с ε={базовый_epsilon}:")
    print(f"  Жёсткая композиция:   k × ε = {жёсткая_композиция:.2f}")
    print(f"  Мягкая композиция:    √(2k×ln(1/δ)) × ε = {мягкая_композиция:.2f}")
    print(f"  Экономия: {(1 - мягкая_композиция / жёсткая_композиция) * 100:.1f}%")

    # Показать рост с k
    print("\n  k   | Жёсткая | Мягкая")
    print("  " + "-" * 35)
    for k_val in [1, 5, 10, 20, 50, 100]:
        жёсткая = k_val * базовый_epsilon
        мягкая = math.sqrt(2 * k_val * math.log(1 / delta)) * базовый_epsilon
        print(f"  {k_val:>3d} | {жёсткая:>7.1f} | {мягкая:.1f}")

    # --- 1d. Rényi Differential Privacy (RDP) ---
    print("\n--- Rényi Differential Privacy (RDP) ---")
    print("RDP сильнее (tight) композиция: RDP_α(M₁∘M₂) = RDP_α(M₁) + RDP_α(M₂)")

    def rdp_laplace(epsilon, alpha):
        """RDP параметр для Лаплас-механизма."""
        if alpha == 1:
            return epsilon  # fallback
        # Упрощённая формула для Лапласа
        return epsilon ** 2 * alpha / 2

    print("\nRDP для Лаплас-механизма (ε=1.0):")
    for alpha in [2, 5, 10, 20, 50]:
        rdp = rdp_laplace(1.0, alpha)
        print(f"  α={alpha:2d}: RDP_α = {rdp:.4f}")

    print("\nВЫВОД: Малый ε = больше приватности, но больше шума.")
    print("Теорема композиции показывает накопление приватного бюджета.")



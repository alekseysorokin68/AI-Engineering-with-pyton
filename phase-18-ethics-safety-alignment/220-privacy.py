# 4. Privacy-Utility Trade-off (privacy budget, noise calibration, attack resistance)
# ==============================================================================

def demo_privacy_utility_tradeoff():
    """Trade-off приватности и полезности: бюджет, калибровка шума, атаки."""
    print("\n" + "=" * 70)
    print("DEMO 4: Privacy-Utility Trade-off")
    print("=" * 70)

    # --- 4a. Privacy Budget ---
    print("\n--- Privacy Budget (приватный бюджет) ---")
    print("Каждый запрос тратит часть бюджета ε. Когда бюджет исчерпан — стоп.")

    общий_бюджет = 2.0  # ε_total = 2.0
    использовано = 0
    запросы = [
        ("Сколько пациентов > 50?", 0.3),
        ("Средний доход?", 0.5),
        ("Мода по диагнозам?", 0.4),
        ("Медиана по возрасту?", 0.6),
        ("Распределение по регионам?", 0.8),
    ]

    print(f"\nОбщий бюджет: ε = {общий_бюджет}")
    print(f"\n{'Запрос':<40s} {'ε запроса':>10s} {'Остаток':>10s} {'Статус':>10s}")
    print("-" * 73)

    for запрос, эпсилон in запросы:
        если_хватит = использовано + эпсилон <= общий_бюджет
        if если_хватит:
            использовано += эпсилон
            статус = "ВЫПОЛНЕН"
        else:
            статус = "ОТКАЗ"
        остаток = общий_бюджет - использовано
        print(f"  {запрос:<38s} {эпсилон:>10.2f} {остаток:>10.2f} {статус:>10s}")

    print(f"\nИтого использовано: {использовано:.2f} из {общий_бюджет}")

    # --- 4b. Noise Calibration ---
    print("\n--- Noise Calibration (калибровка шума) ---")
    print("Равновесие: слишком мало шума → слабая приватность, слишком много → бесполезные данные.")

    реальные_данные = [25, 30, 35, 40, 45, 50, 55, 60]
    среднее_реальное = sum(реальные_данные) / len(реальные_данные)

    print(f"\nРеальные данные: {реальные_данные}")
    print(f"Реальное среднее: {среднее_реальное}")

    def лаплас_шум(scale):
        """Генерация шума из распределения Лапласа."""
        u = random.random() - 0.5
        return -scale * math.copysign(1, u) * math.log(1 - 2 * abs(u))

    print(f"\n{'Epsilon':>8s} {'Сред. шум':>10s} {'MSE':>8s} {'Отношение S/N':>15s}")
    print("-" * 45)

    random.seed(42)
    for эпсилон in [0.01, 0.1, 0.5, 1.0, 2.0, 5.0, 10.0]:
        scale = 1.0 / эпсилон
        зашумлённые = [среднее_реальное + лаплас_шум(scale) for _ in range(1000)]
        mse = sum((x - среднее_реальное) ** 2 for x in зашумлённые) / len(зашумлённые)
        snr = (среднее_реальное ** 2) / mse if mse > 0 else float("inf")
        print(f"  {эпсилон:>6.2f}   {scale:>10.2f}   {mse:>8.2f}   {snr:>15.2f}")

    print("\nВывод: ε → ∞: нет шума, идеальная полезность, нулевая приватность")
    print("       ε → 0: бесконечный шум, идеальная приватность, нулевая полезность")

    # --- 4c. Membership Inference Attack ---
    print("\n--- Membership Inference Attack ---")
    print("Атакующий пытается определить, была ли запись в обучающих данных.")

    # Симуляция confidence распределений
    random.seed(42)

    # Записи из обучающей выборки tend to have higher confidence
    члены_обуч = [random.uniform(0.7, 0.99) for _ in range(50)]
    не_члены = [random.uniform(0.3, 0.8) for _ in range(50)]

    print(f"\nConfidence для членов обучающей выборки:")
    print(f"  Среднее: {sum(члены_обуч)/len(члены_обуч):.3f}, "
          f"Медиана: {sorted(члены_обуч)[25]:.3f}")

    print(f"\nConfidence для НЕ членов:")
    print(f"  Среднее: {sum(не_члены)/len(не_члены):.3f}, "
          f"Медиана: {sorted(не_члены)[25]:.3f}")

    # Порог
    порог = 0.75
    tp = sum(1 for x in члены_обуч if x >= порог)  # true positive
    fp = sum(1 for x in не_члены if x >= порог)     # false positive
    fn = len(члены_обуч) - tp
    tn = len(не_члены) - fp

    точность = (tp + tn) / (tp + fp + fn + tn)
    precision = tp / (tp + fp) if (tp + fp) > 0 else 0
    recall = tp / (tp + fn) if (tp + fn) > 0 else 0

    print(f"\nАтака с порогом = {порог}:")
    print(f"  TP={tp}, FP={fp}, FN={fn}, TN={tn}")
    print(f"  Точность атаки: {точность:.2%}")
    print(f"  Precision: {precision:.3f}, Recall: {recall:.3f}")

    if точность > 0.6:
        print("  → Модель уязвима к membership inference attack!")
    else:
        print("  → Модель устойчива к membership inference attack.")

    # --- 4d. Дифференциальная приватность vs. атака ---
    print("\n--- Защита ДП от атак ---")
    print("С увеличением ε (меньше приватности) атака становится точнее.")

    random.seed(42)
    print(f"\n{'Epsilon':>8s} {'Точность атаки':>15s} {'Защита':>10s}")
    print("-" * 37)

    for эпсилон in [0.1, 0.5, 1.0, 2.0, 5.0, 10.0, 100.0]:
        # Чем больше epsilon, тем меньше шума → атака точнее
        точность_атаки = 0.5 + 0.4 * (1 - math.exp(-эпсилон))
        if точность_атаки > 0.8:
            защита = "Слабая"
        elif точность_атаки > 0.65:
            защита = "Средняя"
        else:
            защита = "Сильная"
        print(f"  {эпсилон:>6.1f}   {точность_атаки:>14.2%}   {защита:>10s}")

    print("\nВЫВОД: Privacy-utility trade-off — фундаментальное ограничение.")
    print("Реальные системы должны балансировать между защитой данных и полезностью модели.")



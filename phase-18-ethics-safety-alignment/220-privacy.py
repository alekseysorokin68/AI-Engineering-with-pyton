def demo_federated_learning():
    """Federated Learning: локальное обучение, агрегация, безопасные вычисления."""
    print("\n" + "=" * 70)
    print("DEMO 2: Federated Learning")
    print("=" * 70)

    # --- 2a. Локальное обучение ---
    print("\n--- Локальное обучение ---")
    print("Каждый клиент обучает модель на своих данных, отправляет обновления.")

    # Симуляция 3 клиентов с разными данными
    random.seed(42)

    клиенты_данные = {
        "Клиент_1 (больница_А)": [(i * 0.1 + random.gauss(0, 0.05), 2 * i * 0.1 + 0.5 + random.gauss(0, 0.1)) for i in range(20)],
        "Клиент_2 (больница_Б)": [(i * 0.15 + random.gauss(0, 0.08), 1.5 * i * 0.15 + 1.0 + random.gauss(0, 0.1)) for i in range(15)],
        "Клиент_3 (больница_В)": [(i * 0.08 + random.gauss(0, 0.03), 3.0 * i * 0.08 + 0.2 + random.gauss(0, 0.15)) for i in range(25)],
    }

    def обучить_линейную(данные, эпохи=50, lr=0.01):
        """Обучение простой линейной модели: y = w*x + b."""
        w = random.uniform(-1, 1)
        b = random.uniform(-1, 1)
        for _ in range(эпохи):
            for x, y in данные:
                y_pred = w * x + b
                ошибка = y_pred - y
                w -= lr * ошибка * x
                b -= lr * ошибка
        return w, b

    локальные_модели = {}
    for название, данные in клиенты_данные.items():
        w, b = обучить_линейную(данные)
        локальные_модели[название] = (w, b)
        print(f"  {название}: y = {w:.4f}x + {b:.4f} ( данных: {len(данные)})")

    # --- 2b. FedAvg (Federated Averaging) ---
    print("\n--- FedAvg: агрегация моделей ---")
    print("Формула: w_глоб = Σ (n_k / n) × w_k (взвешенное среднее)")

    # Взвешенное среднее по количеству данных
    общее_число = sum(len(данные) for данные in клиенты_данные.values())
    w_глоб = 0.0
    b_глоб = 0.0
    for название, данные in клиенты_данные.items():
        вес = len(данные) / общее_число
        w_k, b_k = локальные_модели[название]
        w_глоб += вес * w_k
        b_глоб += вес * b_k
        print(f"  {название}: вес = {вес:.3f}, w = {w_k:.4f}, b = {b_k:.4f}")

    print(f"\n  Глобальная модель: y = {w_глоб:.4f}x + {b_глоб:.4f}")
    print(f"  (суммарные данные: {общее_число})")

    # --- 2c. Secure Aggregation ---
    print("\n--- Secure Aggregation (безопасная агрегация) ---")
    print("Клиенты шифруют обновления; сервер видит только сумму.")

    # Простая эмуляция через XOR-шифрование
    random.seed(42)

    def простое_шифрование(число, ключ):
        """Простое «шифрование» через XOR с ключом (для демонстрации)."""
        байты = str(число).encode()
        зашифровано = bytes(b ^ ключ[i % len(ключ)] for i, b in enumerate(байты))
        return зашифровано

    def простое_дешифрование(зашифровано, ключ):
        """Дешифрование."""
        расшифровано = bytes(b ^ ключ[i % len(ключ)] for i, b in enumerate(зашифровано))
        return float(расшифровано.decode())

    общий_ключ = b"secret_key_123"
    зашифрованные_w = []
    зашифрованные_b = []

    for название, (w, b) in локальные_модели.items():
        заш_w = простое_шифрование(round(w, 4), общий_ключ)
        заш_b = простое_шифрование(round(b, 4), общий_ключ)
        зашифрованные_w.append(заш_w)
        зашифрованные_b.append(заш_b)
        print(f"  {название}: w зашифровано ({len(заш_w)} байт)")

    # Сервер агрегирует (эмуляция через расшифровку)
    сумма_w = sum(простое_дешифрование(z, общий_ключ) for z in зашифрованные_w)
    сумма_b = sum(простое_дешифрование(z, общий_ключ) for z in зашифрованные_b)

    print(f"\n  Сервер получил шифрованные обновления от {len(зашифрованные_w)} клиентов")
    print(f"  После агрегации: w_сред = {сумма_w / len(зашифрованные_w):.4f}")

    # --- 2d. Дифференциальная приватность в FL ---
    print("\n--- DP-Federated Learning ---")
    print("Добавление шума к обновлениям перед отправкой на сервер.")

    def лаплас_шум_fl(scale):
        """Генерация шума из распределения Лапласа."""
        u = random.random() - 0.5
        return -scale * math.copysign(1, u) * math.log(1 - 2 * abs(u))

    epsilons_FL = [0.1, 1.0, 10.0]
    random.seed(42)

    for эпсилон in epsilons_FL:
        зашумлённые_w = []
        for название, (w, b) in локальные_модели.items():
            шум = лаплас_шум_fl(1.0 / эпсилон)
            зашумлённые_w.append(w + шум)

        средний_w = sum(зашумлённые_w) / len(зашумлённые_w)
        print(f"  ε={эпсилон:.1f}: зашумлённые w = {[f'{w:.3f}' for w in зашумлённые_w]}, среднее = {средний_w:.4f}")

    print("\nВЫВОД: Federated Learning позволяет обучать модели без")
    print("передачи сырых данных на центральный сервер.")



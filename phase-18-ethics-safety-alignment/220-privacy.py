def demo_data_anonymization():
    """Анонимизация данных: k-anonymity, l-diversity, t-closeness."""
    print("\n" + "=" * 70)
    print("DEMO 3: Data Anonymization (k-anonymity, l-diversity, t-closeness)")
    print("=" * 70)

    # --- 3a. k-Anonymity ---
    print("\n--- k-Anonymity ---")
    print("Каждая комбинация quasi-identifier'ов встречается ≥ k раз.")

    # Исходная таблица
    исходная = [
        {"ID": 1, "Имя": "Иванов А.", "Возраст": 29, "Индекс": 101000, "Болезнь": "Грипп"},
        {"ID": 2, "Имя": "Петров Б.", "Возраст": 29, "Индекс": 101000, "Болезнь": "Ангина"},
        {"ID": 3, "Имя": "Сидоров В.", "Возраст": 42, "Индекс": 102000, "Болезнь": "Диабет"},
        {"ID": 4, "Имя": "Козлов Г.", "Возраст": 42, "Индекс": 102000, "Болезнь": "Гипертония"},
        {"ID": 5, "Имя": "Новиков Д.", "Возраст": 35, "Индекс": 103000, "Болезнь": "Астма"},
        {"ID": 6, "Имя": "Морозов Е.", "Возраст": 35, "Индекс": 103000, "Болезнь": "Аллергия"},
        {"ID": 7, "Имя": "Волков Ж.", "Возраст": 29, "Индекс": 104000, "Болезнь": "Грипп"},
        {"ID": 8, "Имя": "Зайцев З.", "Возраст": 42, "Индекс": 104000, "Болезнь": "Диабет"},
    ]

    quasi_identifiers = ["Возраст", "Индекс"]

    # Проверка 2-anonymity
    print("\nИсходная таблица ( quasi-identifiers: Возраст, Индекс):")
    groups = collections.Counter()
    for запись in исходная:
        ключ = (запись["Возраст"], запись["Индекс"])
        groups[ключ] += 1

    print(f"\nГруппы (Возраст, Индекс):")
    for ключ, кол in sorted(groups.items()):
        статус = "OK" if кол >= 2 else "УЯЗВИМА"
        print(f"  {ключ}: {кол} записи → {статус}")

    min_k = min(groups.values())
    print(f"\nk-anonymity (k={min_k}):", "ВЫПОЛНЕНА" if min_k >= 2 else "НАРУШЕНА")

    # Анонимизация: группировка по возрасту
    анонимизированные = []
    for запись in исходная:
        новая = запись.copy()
        # Группируем возраст: 20-30, 30-40, 40-50
        возраст = запись["Возраст"]
        if возраст < 30:
            новая["Возраст"] = "20-30"
        elif возраст < 40:
            новая["Возраст"] = "30-40"
        else:
            новая["Возраст"] = "40-50"
        # Группируем индекс
        индекс = запись["Индекс"]
        новая["Индекс"] = f"{индекс // 1000}***"
        анонимизированные.append(новая)

    print("\nАнонимизированная таблица (k=2):")
    print(f"  {'ID':>3} {'Возраст':>10} {'Индекс':>10} {'Болезнь':>12}")
    print("  " + "-" * 40)
    for запись in анонимизированные:
        print(f"  {запись['ID']:>3} {запись['Возраст']:>10} {запись['Индекс']:>10} {запись['Болезнь']:>12}")

    # --- 3b. l-Diversity ---
    print("\n--- l-Diversity ---")
    print("В каждой группе должно быть ≥ l различных значений целевого атрибута.")

    # Проверка l-diversity для квази-групп
    группы = collections.defaultdict(list)
    for запись in исходная:
        ключ = (запись["Возраст"], запись["Индекс"])
        группы[ключ].append(запись["Болезнь"])

    print("\nАнализ l-diversity (целевой атрибут: Болезнь):")
    min_l = float("inf")
    for ключ, болезни in группы.items():
        уникальные = set(болезни)
        l = len(уникальные)
        min_l = min(min_l, l)
        print(f"  Группа {ключ}: {болезни} → l={l} ({уникальные})")

    print(f"\nl-diversity (l={min_l}):", "ВЫПОЛНЕНА" if min_l >= 2 else "НАРУШЕНА")

    # --- 3c. t-Closeness ---
    print("\n--- t-Closeness ---")
    print("Распределение целевого атрибута в группе не должно отличаться")
    print("от распределения в таблице более чем на t.")

    # Распределение болезней в целой таблице
    все_болезни = [з["Болезнь"] for з in исходная]
    общее_распределение = collections.Counter(все_болезни)
    n = len(все_болезни)

    print(f"\nОбщее распределение болезней:")
    for болезнь, кол in общее_распределение.items():
        print(f"  {болезнь}: {кол}/{n} = {кол/n:.3f}")

    # Распределение по группам
    print("\nРаспределение по группам:")

    def расстояние_мад(p, q, домен):
        """Расстояние Манхэттена между распределениями."""
        return sum(abs(p.get(d, 0) - q.get(d, 0)) for d in домен)

    домен = list(общее_распределение.keys())
    общее_расп = {б: кол / n for б, кол in общее_распределение.items()}

    max_t = 0
    for ключ, болезни in группы.items():
        лок_расп = collections.Counter(болезни)
        лок_расп_норм = {б: кол / len(болезни) for б, кол in лок_расп.items()}
        t = расстояние_мад(общее_расп, лок_расп_норм, домен)
        max_t = max(max_t, t)
        print(f"  Группа {ключ}: t = {t:.4f}")

    print(f"\nМаксимальное t: {max_t:.4f}")
    print(f"При t=0.3: {'ВЫПОЛНЕНА' if max_t <= 0.3 else 'НАРУШЕНА'}")
    print(f"При t=0.2: {'ВЫПОЛНЕНА' if max_t <= 0.2 else 'НАРУШЕНА'}")

    # --- 3d. Маскирование (Suppression) ---
    print("\n--- Suppression (маскирование) ---")
    print("Полное удаление уязвимых значений.")

    # Найти группы с единственным значением целевого атрибута
    уязвимые = []
    for ключ, болезни in группы.items():
        if len(set(болезни)) == 1:
            уязвимые.append((ключ, болезни[0]))

    print(f"\nУязвимые группы (одно значение болезни):")
    for ключ, болезнь in уязвимые:
        print(f"  {ключ}: единственная болезнь = {болезнь}")
        print(f"    → Решение: маскирование или объединение с другой группой")

    print("\nВЫВОД: k-anonymity, l-diversity и t-closeness — три уровня")
    print("защиты данных от повторной идентификации.")



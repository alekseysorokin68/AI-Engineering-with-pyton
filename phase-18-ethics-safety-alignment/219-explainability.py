def demo_intrinsic_interpretability():
    """Встроенная интерпретируемость: линейные модели, деревья решений, attention."""
    print("\n" + "=" * 70)
    print("DEMO 2: Intrinsic Interpretability")
    print("=" * 70)

    # --- 2a. Линейная модель: коэффициенты = интерпретация ---
    print("\n--- Линейная модель ---")
    print("Коэффициенты напрямую интерпретируемы: изменение y на w_i при изменении x_i на 1.")

    # Простая линейная регрессия: y = 2*x1 + 0.5*x2 - 1.0*x3 + 0.3
    random.seed(42)
    n = 100
    данные = []
    for _ in range(n):
        x1 = random.uniform(0, 10)
        x2 = random.uniform(0, 5)
        x3 = random.uniform(-2, 2)
        y = 2 * x1 + 0.5 * x2 - 1.0 * x3 + 0.3 + random.gauss(0, 0.5)
        данные.append((x1, x2, x3, y))

    # Градиентный спуск для обучения
    w = [0.0, 0.0, 0.0]
    b = 0.0
    lr = 0.001
    for эпоха in range(500):
        for x1, x2, x3, y in данные:
            y_pred = w[0] * x1 + w[1] * x2 + w[2] * x3 + b
            ошибка = y_pred - y
            w[0] -= lr * ошибка * x1
            w[1] -= lr * ошибка * x2
            w[2] -= lr * ошибка * x3
            b -= lr * ошибка

    print(f"\nОбученные коэффициенты:")
    print(f"  w_ x1 (реальное=2.0): {w[0]:.4f}")
    print(f"  w_ x2 (реальное=0.5): {w[1]:.4f}")
    print(f"  w_ x3 (реальное=-1.0): {w[2]:.4f}")
    print(f"  intercept (реальное=0.3): {b:.4f}")

    # Интерпретация: «Если увеличить x1 на 1, то y увеличится на w[0]»
    print(f"\nИнтерпретация: +1 к x1 → y изменится на {w[0]:+.4f}")
    print(f"Интерпретация: +1 к x2 → y изменится на {w[1]:+.4f}")
    print(f"Интерпретация: +1 к x3 → y изменится на {w[2]:+.4f}")

    # --- 2b. Дерево решений ---
    print("\n--- Дерево решений ---")
    print("Каждый узел = правило: if feature < threshold → влево, иначе → вправо.")

    # Простое дерево: классификация «одобрить кредит»
    class УзелДерева:
        def __init__(self, признак=None, порог=None, лево=None, право=None, класс=None):
            self.признак = признак
            self.порог = порог
            self.лево = лево
            self.право = право
            self.класс = класс

    # Ручная конструкция дерева
    #         [доход < 40000?]
    #        /               \
    #  [возраст < 25?]    [кредит < 3?]
    #   /       \            /       \
    # Нет      Да          Да       Нет
    дерево = УзелДерева(
        признак="доход", порог=40000,
        лево=УзелДерева(
            признак="возраст", порог=25,
            лево=УзелДерева(класс="Отказать"),
            право=УзелДерева(класс="Условно"),
        ),
        право=УзелДерева(
            признак="кредит", порог=3,
            лево=УзелДерева(класс="Условно"),
            право=УзелДерева(класс="Одобрить"),
        ),
    )

    def предсказать_дерево(узел, данные_клиента):
        """Рекурсивное предсказание по дереву решений."""
        if узел.класс is not None:
            return узел.класс
        if данные_клиента[узел.признак] < узел.порог:
            return предсказать_дерево(узел.лево, данные_клиента)
        else:
            return предсказать_дерево(узел.право, данные_клиента)

    клиенты = [
        {"доход": 35000, "возраст": 30, "кредит": 4},
        {"доход": 60000, "возраст": 22, "кредит": 5},
        {"доход": 25000, "возраст": 20, "кредит": 2},
        {"доход": 80000, "возраст": 45, "кредит": 7},
    ]

    print("\nДерево решений для одобрения кредита:")
    print("         [доход < 40000?]")
    print("        /               \\")
    print("  [возраст < 25?]    [кредит < 3?]")
    print("   /       \\            /       \\")
    print(" Нет      Да          Да       Нет")
    print()

    for клиент in клиенты:
        результат = предсказать_дерево(дерево, клиент)
        print(f"  Доход={клиент['доход']:>6}, Возраст={клиент['возраст']}, Кредит={клиент['кредит']} → {результат}")

    # --- 2c. Attention weights ---
    print("\n--- Attention (внимание) ---")
    print("Attention: для каждого выходного токена вычисляется вес каждого входного.")

    # Пример attention для фразы "кошка сидит на ковре"
    токены_вход = ["кошка", "сидит", "на", "ковре"]
    токены_выход = ["кошка", "сидит", "ковре"]

    # Матрица attention (нормализованные веса)
    random.seed(42)
    raw_scores = []
    for i in range(len(токены_выход)):
        row = []
        for j in range(len(токены_вход)):
            # Близость токенов через хеш (эмуляция)
            h_i = sum(ord(c) for c in токены_выход[i])
            h_j = sum(ord(c) for c in токены_вход[j])
            row.append((h_i * h_j) % 100 / 100.0 + random.random() * 0.1)
        raw_scores.append(row)

    # Softmax по строкам
    attention = []
    for row in raw_scores:
        макс = max(row)
        exp_row = [math.exp(s - макс) for s in row]
        сумма = sum(exp_row)
        attention.append([e / сумма for e in exp_row])

    print("\nМатрица Attention (выход × вход):")
    заголовок = f"{'':>10}" + "".join(f"{t:>10}" for t in токены_вход)
    print(заголовок)
    for i, токен in enumerate(токены_выход):
        строка = f"{токен:>10}" + "".join(f"{w:10.3f}" for w in attention[i])
        print(строка)

    # Для каждого выходного токена — топ-1 входной
    print("\nСамое сильное внимание для каждого токена:")
    for i, токен_вых in enumerate(токены_выход):
        индекс_макс = attention[i].index(max(attention[i]))
        print(f"  '{токен_вых}' → '{токены_вход}[индекс={индекс_макс}]' "
              f"(вес={attention[i][индекс_макс]:.3f})")

    # --- 2d. Сравнение методов ---
    print("\n--- Сравнение методов интерпретируемости ---")
    print("┌─────────────────────┬──────────────────┬───────────────────┐")
    print("│ Метод               │ Интерпретируемость│ Мощность модели   │")
    print("├─────────────────────┼──────────────────┼───────────────────┤")
    print("│ Линейная модель     │ Высокая          │ Низкая            │")
    print("│ Дерево решений     │ Средняя          │ Средняя           │")
    print("│ Attention           │ Средняя          │ Высокая           │")
    print("│ Глубокая нейросеть │ Низкая           │ Очень высокая     │")
    print("└─────────────────────┴──────────────────┴───────────────────┘")

    print("\nВЫВОД: Встроенная интерпретируемость — когда модель сама")
    print("«объясняет» себя через свою структуру (веса, дерево, attention).")



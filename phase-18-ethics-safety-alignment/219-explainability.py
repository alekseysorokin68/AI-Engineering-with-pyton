# 1. Post-Hoc Explanation (LIME, SHAP concepts, feature importance)
# ==============================================================================

def demo_post_hoc_explanation():
    """Пост-hoc объяснения: LIME, SHAP, feature importance."""
    print("=" * 70)
    print("DEMO 1: Post-Hoc Explanation (LIME, SHAP, feature importance)")
    print("=" * 70)

    # --- 1a. Простая модель для объяснения ---
    # Линейная модель: prediction = w0 + w1*x1 + w2*x2 + w3*x3
    weights = {
        "intercept": 0.5,
        "возраст": 0.03,
        "доход": 0.0001,
        "кредит_история": -0.8,
    }

    def предсказать(features):
        """Вычисление предсказания по линейной модели."""
        результат = weights["intercept"]
        for key, val in features.items():
            результат += weights.get(key, 0) * val
        return результат

    # Тестовый пример
    пример = {"возраст": 30, "доход": 50000, "кредит_история": 1}
    предсказание = предсказать(пример)
    print(f"\nМодель: prediction = {weights['intercept']} + 0.03*возраст + 0.0001*доход - 0.8*кредит_история")
    print(f"Пример: {пример}")
    print(f"Предсказание: {предсказание:.4f}")

    # --- 1b. LIME: локальная интерпретируемая модель-объяснение ---
    print("\n--- LIME (Local Interpretable Model-agnostic Explanations) ---")
    print("LIME обучает локальную линейную модель вокруг точки объяснения.")

    # Генерация локальных соседей (возмущения исходного примера)
    # Нормализуем признаки для корректного вычисления расстояния
    соседи = []
    random.seed(42)
    for _ in range(20):
        возмущённый = {}
        for key, val in пример.items():
            шум = random.gauss(0, 0.1 * abs(val) + 0.5)
            возмущённый[key] = val + шум
        предс = предсказать(возмущённый)
        # Нормализованное расстояние (делаем на масштаб каждого признака)
        масштаб = {"возраст": 10.0, "доход": 10000.0, "кредит_история": 1.0}
        расстояние = math.sqrt(sum(((возмущённый[k] - пример[k]) / масштаб[k]) ** 2 for k in пример))
        вес = math.exp(-(расстояние ** 2) / 2)
        соседи.append((возмущённый, предс, вес))

    print(f"Сгенерировано {len(соседи)} локальных соседей")
    min_w = min(w for _, _, w in соседи)
    max_w = max(w for _, _, w in соседи)
    print(f"Мин. вес: {min_w:.4f}, Макс. вес: {max_w:.4f}")

    # Взвешенная корреляция признака с предсказанием
    локальная_важность = {}
    for key in пример:
        сумма_весов = sum(w for _, _, w in соседи)
        среднее_x = sum(сосед[key] * w for сосед, _, w in соседи) / сумма_весов
        среднее_y = sum(предс * w for _, предс, w in соседи) / сумма_весов
        ковариация = sum((сосед[key] - среднее_x) * (предс - среднее_y) * w for сосед, _, w in соседи) / сумма_весов
        дисперсия_x = sum((сосед[key] - среднее_x) ** 2 * w for сосед, _, w in соседи) / сумма_весов
        if дисперсия_x > 1e-10:
            локальная_важность[key] = ковариация / дисперсия_x
        else:
            локальная_важность[key] = 0

    print("\nLIME локальная важность признаков:")
    for key, val in sorted(локальная_важность.items(), key=lambda x: abs(x[1]), reverse=True):
        знак = "+" if val > 0 else "-"
        print(f"  {key:18s}: {знак}{abs(val):.6f}")

    # --- 1c. SHAP: Additive Feature Attribution ---
    print("\n--- SHAP (SHapley Additive exPlanations) ---")
    print("SHAP на основе теории игр Шепли: какой вклад даёт каждый признак.")

    # Полное значение: prediction с всеми признаками
    полное_значение = предсказать(пример)

    # Значение отсутствия (baseline): prediction без признаков
    значение_отсутствия = weights["intercept"]

    # Расчёт SHAP-значений через перебор подмножеств (2^3 = 8 комбинаций)
    признаки = list(пример.keys())
    shap_значения = {k: 0.0 for k in признаки}
    количество = {k: 0 for k in признаки}

    для_подмножеств = []
    for маска in range(1 << len(признаки)):
        подмножество = [признаки[i] for i in range(len(признаки)) if маска & (1 << i)]
        для_подмножеств.append(подмножество)

    for подмножество in для_подмножеств:
        # Предсказание с данным подмножеством
        feat = {k: пример[k] for k in подмножество}
        предс_подмножество = предсказать(feat)
        размер = len(подмножество)

        # Добавляем вклад для каждого признака в подмножестве
        for признак in подмножество:
            # Вклад признака = (предсказание с) - (предсказание без)
            feat_без = {k: пример[k] for k in подмножество if k != признак}
            предс_без = предсказать(feat_без)
            shap_значения[признак] += (предс_подмножество - предс_без)

    # Нормализация
    for признак in признаки:
        shap_значения[признак] /= (2 ** (len(признаки) - 1))

    print(f"Базовое значение (intercept): {значение_отсутствия:.4f}")
    print(f"Итоговое предсказание:         {полное_значение:.4f}")
    print(f"Сумма SHAP-значений:           {значение_отсутствия + sum(shap_значения.values()):.4f}")
    print("\nSHAP-значения:")
    for key, val in sorted(shap_значения.items(), key=lambda x: abs(x[1]), reverse=True):
        знак = "+" if val > 0 else "-"
        print(f"  {key:18s}: {знак}{abs(val):.4f}")

    # --- 1d. Permutation Feature Importance ---
    print("\n--- Permutation Feature Importance ---")
    print("Метод: перемешиваем признак и смотрим на падение точности.")

    # Базовая ошибка (MSE на контрольных данных)
    random.seed(42)
    контроль = []
    for _ in range(50):
        контроль.append({
            "возраст": random.uniform(20, 60),
            "доход": random.uniform(20000, 100000),
            "кредит_история": random.choice([0, 1]),
        })

    базовая_ошибка = sum((предсказать(x) - 3) ** 2 for x in контроль) / len(контроль)

    print(f"\nБазовая MSE (целевое значение = 3): {базовая_ошибка:.4f}")

    важность = {}
    for признак in признаки:
        перемешанные = []
        for x in контроль:
            new_x = x.copy()
            random.shuffle([new_x[признак]])  # перемешиваем один признак
            new_x[признак] = random.choice([xx[признак] for xx in контроль])
            перемешанные.append(new_x)

        ошибка_после = sum((предсказать(x) - 3) ** 2 for x in перемешанные) / len(перемешанные)
        важность[признак] = ошибка_после - базовая_ошибка

    print("\nPermutation Importance (рост ошибки = важность):")
    for key, val in sorted(важность.items(), key=lambda x: x[1], reverse=True):
        print(f"  {key:18s}: {val:+.6f}")

    print("\nВЫВОД: Все три метода (LIME, SHAP, Permutation) показывают")
    print("вклад каждого признака в предсказание модели.")



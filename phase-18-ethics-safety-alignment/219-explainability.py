def demo_explanation_formats():
    """Форматы объяснений: важность признаков, контрфакты, правила."""
    print("\n" + "=" * 70)
    print("DEMO 3: Explanation Formats")
    print("=" * 70)

    # --- 3a. Feature Importance ( bar-chart в тексте) ---
    print("\n--- Feature Importance (текстовая гистограмма) ---")

    важность = {
        "кредитный_рейтинг": 0.35,
        "доход": 0.25,
        "возраст": 0.15,
        "срок_кредита": 0.12,
        "наличие_недвижимости": 0.08,
        "образование": 0.05,
    }

    print("\nВажность признаков для решения «выдать кредит»:")
    макс = max(важность.values())
    for название, значение in sorted(важность.items(), key=lambda x: x[1], reverse=True):
        длина_бара = int(значение / макс * 40)
        бар = "█" * длина_бара
        print(f"  {название:25s} {бар} {значение:.2f}")

    # --- 3b. Counterfactual explanations ---
    print("\n--- Counterfactual Explanations ---")
    print("Контрфакт: «Что нужно изменить, чтобы результат изменился?»")

    # Клиент, которому отказали
    клиент = {
        "возраст": 22,
        "доход": 28000,
        "кредитный_рейтинг": 580,
        "срок_кредита_мес": 60,
    }

    # Пороговые значения для одобрения
    пороги = {
        "возраст": 25,
        "доход": 35000,
        "кредитный_рейтинг": 620,
        "срок_кредита_мес": 48,
    }

    # Направление: что нужно для одобрения (больше = лучше)
    направление = {
        "возраст": "больше",
        "доход": "больше",
        "кредитный_рейтинг": "больше",
        "срок_кредита_мес": "меньше",
    }

    print(f"\nТекущее решение: ОТКАЗ")
    print(f"Текущие значения клиента: {клиент}")
    print(f"\nКонтрфакт: минимальные изменения для ОДОБРЕНИЯ:")

    контрфакт = клиент.copy()
    изменения = []
    for признак, порог in пороги.items():
        текущее = клиент[признак]
        if направление[признак] == "больше" and текущее < порог:
            разница = порог - текущее
            контрфакт[признак] = порог
            изменения.append((признак, текущее, порог, разница))
        elif направление[признак] == "меньше" and текущее > порог:
            разница = текущее - порог
            контрфакт[признак] = порог
            изменения.append((признак, текущее, порог, -разница))

    for признак, старое, новое, разница in изменения:
        направление_текст = "увеличить" if разница > 0 else "уменьшить"
        print(f"  {признак}: {старое} → {новое} ({направление_текст} на {abs(разница):.0f})")

    print(f"\nНовые значения: {контрфакт}")

    # --- 3c. Rule-based explanations ---
    print("\n--- Rule-based Explanations (IF-THEN правила) ---")

    правила = [
        ("ЕСЛИ кредитный_рейтинг > 700 ТОГДА одобрить", 0.95, "высокая"),
        ("ЕСЛИ доход > 50000 И кредитный_рейтинг > 600 ТОГДА одобрить", 0.88, "средняя"),
        ("ЕСЛИ возраст < 25 И доход < 30000 ТОГДА отказать", 0.82, "средняя"),
        ("ЕСЛИ срок_кредита > 60 И кредитный_рейтинг < 550 ТОГДА отказать", 0.91, "высокая"),
    ]

    print("\nИзвлечённые правила:")
    for правило, точность, надёжность in правила:
        print(f"  {правило}")
        print(f"    → Точность: {точность:.0%}, Надёжность: {надёжность}")

    # --- 3d. Confidence scores ---
    print("\n--- Confidence Scores ---")
    print("Вместо бинарного ответа — вероятность каждого класса.")

    сценарии = [
        {"описание": "Студент, 20 лет, доход 15000", "вероятности": {"Отказать": 0.85, "Условно": 0.12, "Одобрить": 0.03}},
        {"описание": "Специалист, 35 лет, доход 80000", "вероятности": {"Отказать": 0.05, "Условно": 0.15, "Одобрить": 0.80}},
        {"описание": "Пенсионер, 65 лет, доход 25000", "вероятности": {"Отказать": 0.30, "Условно": 0.50, "Одобрить": 0.20}},
    ]

    for сц in сценарии:
        print(f"\n  {сц['описание']}:")
        for класс, вер in sorted(сц['вероятности'].items(), key=lambda x: x[1], reverse=True):
            длина = int(вер * 30)

def demo_user_studies():
    """Исследования пользователей: удовлетворённость, доверие, ментальные модели."""
    print("\n" + "=" * 70)
    print("DEMO 4: User Studies — explanation satisfaction, trust, mental models")
    print("=" * 70)

    # --- 4a. Explanation Satisfaction Survey ---
    print("\n--- Explanation Satisfaction (анкета удовлетворённости) ---")
    print("5-балльная шкала (1=полностью не согласен, 5=полностью согласен)")

    random.seed(42)

    анкета = [
        "Объяснение помогло понять решение модели",
        "Объяснение было достаточно подробным",
        "Я доверяю объяснению",
        "Объяснение легко интерпретировать",
        "Объяснение изменило моё мнение о модели",
    ]

    # Группа A: LIME-объяснения
    группа_A = []
    for вопрос in анкета:
        оценки = [random.randint(3, 5) for _ in range(10)]
        группа_A.append((вопрос, sum(оценки) / len(оценки)))

    # Группа B: контрфактные объяснения
    группа_B = []
    for вопрос in анкета:
        оценки = [random.randint(2, 5) for _ in range(10)]
        группа_B.append((вопрос, sum(оценки) / len(оценки)))

    print(f"\n{'Вопрос':<48s} {'LIME':>6s} {'Контрф.':>8s}")
    print("-" * 65)
    for (вопрос_a, средняя_a), (_, средняя_b) in zip(группа_A, группа_B):
        print(f"  {вопрос_a:<46s} {средняя_a:6.2f} {средняя_b:8.2f}")

    средняя_A = sum(s for _, s in группа_A) / len(группа_A)
    средняя_B = sum(s for _, s in группа_B) / len(группа_B)
    print(f"\n  {'СРЕДНЯЯ':46s} {средняя_A:6.2f} {средняя_B:8.2f}")

    # --- 4b. Trust Calibration ---
    print("\n--- Trust Calibration (калибровка доверия) ---")
    print("Идеальное доверие: высокое когда модель права, низкое когда права.")

    random.seed(42)

    # Симуляция: модель даёт предсказания,部分 из них неверны
    результаты = []
    for i in range(20):
        модель_верна = random.random() < 0.75  # 75% точность
        доверие_пользователя = random.uniform(0.3, 1.0)  # как сильно доверяет
        if модель_верна:
            доверие_пользователя = max(0.5, доверие_пользователя)
        else:
            доверие_пользователя = min(0.6, доверие_пользователя)
        результаты.append((модель_верна, доверие_пользователя))

    верные_доверие = [d for верно, d in результаты if верно]
    неверные_доверие = [d for верно, d in результаты if not верно]

    сред_верные = sum(верные_доверие) / len(верные_доверие) if верные_доверие else 0
    сред_неверные = sum(неверные_доверие) / len(неверные_доверие) if неверные_доверие else 0

    print(f"\nВерных предсказаний: {len(верные_доверие)}/20")
    print(f"Среднее доверие (модель верна):  {сред_верные:.3f}")
    print(f"Среднее доверие (модель неверна): {сред_неверные:.3f}")
    print(f"Калибровка (разница):             {сред_верные - сред_неверные:.3f}")

    if сред_верные - сред_неверные > 0.15:
        print("→ Хорошая калибровка: пользователи доверяют больше когда модель права")
    else:
        print("→ Слабая калибровка: доверие не адекватно качеству модели")

    # --- 4c. Mental Models ---
    print("\n--- Mental Models (ментальные модели пользователей) ---")
    print("Какие представления имеют пользователи о работе модели?")

    ментальные_модели = {
        "Линейная модель": {"понимание": 0.85, "корректность": 0.90, "количество_пользователей": 12},
        "Дерево решений": {"понимание": 0.72, "корректность": 0.85, "количество_пользователей": 10},
        "Нейросеть (без объяснений)": {"понимание": 0.25, "корректность": 0.30, "количество_пользователей": 15},
        "Нейросеть + LIME": {"понимание": 0.58, "корректность": 0.65, "количество_пользователей": 14},
        "Нейросеть + контрфакты": {"понимание": 0.68, "корректность": 0.72, "количество_пользователей": 13},
    }

    print(f"\n{'Модель':<35s} {'Понимание':>10s} {'Коррект.':>9s} {'N':>4s}")
    print("-" * 62)
    for название, данные in ментальные_модели.items():
        п = данные["понимание"]
        к = данные["корректность"]
        н = данные["количество_пользователей"]
        print(f"  {название:<33s} {п:>9.0%} {к:>9.0%} {н:>4d}")

    print("\nВывод: Объяснения (LIME/контрфакты) значительно повышают")
    print("корректность ментальной модели пользователей о нейросетях.")

    # --- 4d. Explanation fidelity metrics ---
    print("\n--- Fidelity Metrics (метрики качества объяснений) ---")

    def фидельность(объяснение, модель, данные):
        """Мера того, насколько объяснение точно отражает модель."""
        правильных = 0
        for x in данные:
            # Объяснение предсказывает «важный признак»
            предсказание_модели = сумма_по_важным(x, модель)
            предсказание_объяснения = сумма_по_важным(x, объяснение)
            if предсказание_модели == предсказание_объяснения:
                правильных += 1
        return правильных / len(данные)

    def сумма_по_важным(данные, модель):
        """Упрощённое предсказание по важным признакам."""
        порог = 0.5
        значение = sum(данные[k] * модель.get(k, 0) for k in модель)
        return 1 if значение > порог else 0

    # Настоящая модель
    настоящая_модель = {"признак_A": 0.4, "признак_B": 0.3, "признак_C": 0.2}

    объяснения = {
        "Идеальное": {"признак_A": 0.4, "признак_B": 0.3, "признак_C": 0.2},
        "Хорошее": {"признак_A": 0.35, "признак_B": 0.30, "признак_C": 0.20},
        "Плохое": {"признак_A": 0.1, "признак_B": 0.1, "признак_C": 0.5},
        "Случайное": {"признак_A": 0.25, "признак_B": 0.25, "признак_C": 0.25},
    }

    random.seed(42)
    тест_данные = []
    for _ in range(100):
        тест_данные.append({
            "признак_A": random.random(),
            "признак_B": random.random(),
            "признак_C": random.random(),
        })

    print(f"\n{'Объяснение':<18s} {'Fidelity':>10s}")
    print("-" * 30)
    for название, объясн in объяснения.items():
        ф = фидельность(объясн, настоящая_модель, тест_данные)
        print(f"  {название:<16s} {ф:>10.0%}")

    print("\nВЫВОД: Качество объяснений (fidelity) напрямую влияет на")
    print("доверие пользователей и корректность их ментальных моделей.")


